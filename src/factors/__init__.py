# 注册多时间框架因子
def register_multi_timeframe_factors(config: dict = None):
    """注册多时间框架因子

    Args:
        config: 配置字典，包含multi_timeframe_factors配置
    """
    try:
        manager = get_factor_manager()

        # 如果配置中启用了多时间框架因子
        if config is None:
            from config.config_manager import get_config
            config = get_config()

        mtf_config = config.get('system_settings', {}).get('multi_timeframe_factors', {})

        if not mtf_config.get('enabled', True):
            print("多时间框架因子未启用")
            return

        # 注册RSI因子
        rsi_factor = MultiTimeframeRSIFactor(mtf_config)
        manager.register_factor(rsi_factor)

        # 注册MACD因子
        macd_factor = MultiTimeframeMACDFactor(mtf_config)
        manager.register_factor(macd_factor)

        print(f"[OK] Multi-timeframe factors registered: {rsi_factor.name}, {macd_factor.name}")

    except Exception as e:
        print(f"注册多时间框架因子失败: {e}")
        import traceback
        traceback.print_exc()

