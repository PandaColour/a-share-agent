def validate_factors(factor_names: list, test_symbols: list, days_back: int = 30):
    """批量验证因子的便捷函数"""
    results = {}
    for factor_name in factor_names:
        try:
            result = validate_factor_simple(factor_name, test_symbols, days_back)
            results[factor_name] = result
        except Exception as e:
            results[factor_name] = {"error": str(e)}
    return results

def get_auto_factor_summary():
    """获取自动生成因子的摘要"""
    generator = create_auto_factor_generator()
    return generator.get_generated_factors_summary()

def generate_additional_factors(template_count: int = 3, ai_count: int = 2):
    """生成额外的因子"""
    return generate_new_factors(template_count, ai_count)

