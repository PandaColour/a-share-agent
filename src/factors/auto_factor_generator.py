    def _estimate_factor_effectiveness(self, template: FactorTemplate, parameters: Dict) -> float:
        """估算因子有效性"""
        base_effectiveness = {
            1: 0.45,  # 简单因子
            2: 0.55,  # 中等复杂因子  
            3: 0.65,  # 复杂因子
            4: 0.70,  # 很复杂因子
            5: 0.75   # 最复杂因子
        }.get(template.complexity_level, 0.50)
        
        # 根据参数调整
        param_adjustment = 0.0
        for param_name, value in parameters.items():
            if param_name.endswith('period'):
                # 周期参数：中等周期通常更有效
                if 10 <= value <= 30:
                    param_adjustment += 0.05
                elif value < 5 or value > 60:
                    param_adjustment -= 0.05
        
        # 添加随机性
        random_factor = random.uniform(-0.1, 0.1)
        
        final_effectiveness = base_effectiveness + param_adjustment + random_factor
        return max(0.3, min(0.9, final_effectiveness))  # 限制在[0.3, 0.9]区间
    
    def _generate_unique_id(self) -> str:
        """生成唯一ID"""
        timestamp = datetime.now().strftime("%m%d%H%M")
        random_part = ''.join(random.choices(string.ascii_lowercase + string.digits, k=4))
        return f"{timestamp}{random_part}"
    
    def register_generated_factors(self, factor_infos: List[GeneratedFactorInfo]) -> int:
        """注册生成的因子到因子管理器"""
        registered_count = 0
        
        for factor_info in factor_infos:
            try:
                # 创建计算函数
                calculation_func = self._create_calculation_function(factor_info)
                
                # 创建自动生成因子实例
                auto_factor = AutoGeneratedFactor(factor_info, calculation_func)
                
                # 注册到因子管理器
                self.factor_manager.register_factor(auto_factor)
                self.generated_factors.append(auto_factor)
                
                registered_count += 1
                logger.info(f"成功注册因子: {factor_info.factor_name}")
                
            except Exception as e:
                logger.error(f"注册因子 {factor_info.factor_name} 失败: {e}")
        
        logger.info(f"成功注册 {registered_count}/{len(factor_infos)} 个自动生成因子")
        return registered_count
    
    def _create_calculation_function(self, factor_info: GeneratedFactorInfo) -> Callable:
        """根据因子信息创建计算函数"""
        
        # 根据生成方法选择不同的函数创建策略
        if factor_info.generation_method == GenerationMethod.TEMPLATE_BASED:
            return self._create_template_based_function(factor_info)
        elif factor_info.generation_method == GenerationMethod.AI_ENHANCED:
            return self._create_ai_enhanced_function(factor_info)
        else:
            return self._create_default_function(factor_info)
    
    def _create_template_based_function(self, factor_info: GeneratedFactorInfo) -> Callable:
        """为模板生成的因子创建计算函数"""
        
        # 根据模板ID选择对应的计算逻辑
        template_id = factor_info.template_used
        params = factor_info.parameters
        
        if template_id == "momentum_simple":
            def calc_func(data, symbol, **kwargs):
                period = params.get('period', 20)
                closes = data['price']['Close'].tail(period + 1)
                if len(closes) < 2:
                    return 0.0
                return (closes.iloc[-1] - closes.iloc[0]) / closes.iloc[0]
            return calc_func
            
        elif template_id == "volatility_rolling":
            def calc_func(data, symbol, **kwargs):
                period = params.get('period', 20)
                closes = data['price']['Close'].tail(period)
                if len(closes) < 2:
                    return 0.0
                returns = closes.pct_change().dropna()
                return returns.std()
            return calc_func
            
        elif template_id == "volume_price_correlation":
            def calc_func(data, symbol, **kwargs):
                period = params.get('period', 20)
                df_price = data['price'].tail(period)
                df_volume = data['volume'].tail(period)
                if len(df_price) < 5 or len(df_volume) < 5:
                    return 0.0
                
                price_changes = df_price['Close'].pct_change().dropna()
                volume_changes = df_volume['Volume'].pct_change().dropna()
                
                min_len = min(len(price_changes), len(volume_changes))
                if min_len < 3:
                    return 0.0
                    
                price_changes = price_changes.tail(min_len)
                volume_changes = volume_changes.tail(min_len)
                
                correlation = np.corrcoef(price_changes, volume_changes)[0, 1]
                return 0.0 if np.isnan(correlation) else correlation
            return calc_func
            
        elif template_id == "mean_reversion":
            def calc_func(data, symbol, **kwargs):
                period = params.get('period', 20)
                threshold = params.get('threshold', 0.05)
                closes = data['price']['Close'].tail(period + 1)
                if len(closes) < period:
                    return 0.0
                
                current_price = closes.iloc[-1]
                moving_avg = closes.iloc[:-1].mean()
                
                if moving_avg == 0:
                    return 0.0
                    
                deviation = (current_price - moving_avg) / moving_avg
                
                if abs(deviation) > threshold:
                    return -deviation
                else:
                    return 0.0
            return calc_func
            
        elif template_id == "technical_pattern":
            def calc_func(data, symbol, **kwargs):
                lookback = params.get('lookback', 30)
                sensitivity = params.get('sensitivity', 0.02)
                
                df = data['price'].tail(lookback)
                if len(df) < 10:
                    return 0.0
                
                highs = df['High'].values
                lows = df['Low'].values
                closes = df['Close'].values
                
                high_peaks, _ = find_peaks(highs, distance=3)
                low_peaks, _ = find_peaks(-lows, distance=3)
                
                score = 0.0
                current_price = closes[-1]
                
                if len(high_peaks) > 0:
                    resistance_levels = highs[high_peaks]
                    nearest_resistance = resistance_levels[np.argmin(np.abs(resistance_levels - current_price))]
                    if nearest_resistance > current_price:
                        resistance_distance = (nearest_resistance - current_price) / current_price
                        if resistance_distance < sensitivity:
                            score -= 0.5
                
                if len(low_peaks) > 0:
                    support_levels = lows[low_peaks]
                    nearest_support = support_levels[np.argmin(np.abs(support_levels - current_price))]
                    if nearest_support < current_price:
                        support_distance = (current_price - nearest_support) / current_price
                        if support_distance < sensitivity:
                            score += 0.5
                
                return score
            return calc_func
        
        # 默认函数
        return self._create_default_function(factor_info)
    
    def _create_ai_enhanced_function(self, factor_info: GeneratedFactorInfo) -> Callable:
        """为AI增强因子创建计算函数"""
        factor_name = factor_info.factor_name
        
        if "momentum_volatility" in factor_name:
            def calc_func(data, symbol, **kwargs):
                try:
                    closes = data['price']['Close'].tail(25)
                    if len(closes) < 20:
                        return 0.0
                    
                    short_momentum = (closes.iloc[-1] - closes.iloc[-5]) / closes.iloc[-5]
                    long_momentum = (closes.iloc[-1] - closes.iloc[-20]) / closes.iloc[-20]
                    
                    recent_returns = closes.pct_change().tail(10).dropna()
                    volatility = recent_returns.std() if len(recent_returns) > 1 else 0.1
                    
                    if volatility > 0.03:
                        momentum_score = short_momentum * 0.7 + long_momentum * 0.3
                    else:
                        momentum_score = short_momentum * 0.4 + long_momentum * 0.6
                    
                    adjusted_score = momentum_score / (1 + volatility * 10)
                    return np.tanh(adjusted_score * 5)
                except Exception:
                    return 0.0
            return calc_func
            
        elif "volume_pattern" in factor_name:
            def calc_func(data, symbol, **kwargs):
                try:
                    price_df = data['price'].tail(15)
                    volume_df = data['volume'].tail(15)
                    
                    if len(price_df) < 10 or len(volume_df) < 10:
                        return 0.0
                    
                    closes = price_df['Close'].values
                    volumes = volume_df['Volume'].values
                    
                    price_changes = np.diff(closes) / closes[:-1]
                    volume_changes = np.diff(volumes) / volumes[:-1]
                    
                    volume_mean = np.mean(volumes)
                    volume_std = np.std(volumes)
                    abnormal_volume_mask = volumes > (volume_mean + 1.5 * volume_std)
                    
                    pattern_score = 0.0
                    
                    if np.any(abnormal_volume_mask):
                        abnormal_indices = np.where(abnormal_volume_mask)[0]
                        for idx in abnormal_indices[-3:]:
                            if idx > 0:
                                price_impact = price_changes[idx-1]
                                volume_impact = volume_changes[idx-1]
                                
                                if price_impact * volume_impact > 0:
                                    pattern_score += 0.3
                                else:
                                    pattern_score -= 0.2
                    
                    return np.tanh(pattern_score)
                except Exception:
                    return 0.0
            return calc_func
        
        # 默认返回
        return self._create_default_function(factor_info)
    
    def _create_default_function(self, factor_info: GeneratedFactorInfo) -> Callable:
        """创建默认计算函数"""
        def default_calc_func(data, symbol, **kwargs):
            try:
                closes = data['price']['Close'].tail(20)
                if len(closes) < 2:
                    return 0.0
                return (closes.iloc[-1] - closes.iloc[0]) / closes.iloc[0]
            except Exception:
                return 0.0
        
        return default_calc_func
    
    def generate_and_register_factors(self, template_count: int = 3, ai_count: int = 2) -> Dict[str, int]:
        """生成并注册新因子到系统"""
        logger.info(f"开始自动生成因子：模板因子 {template_count} 个，AI因子 {ai_count} 个")
        
        results = {
            "template_generated": 0,
            "template_registered": 0,
            "ai_generated": 0,
            "ai_registered": 0,
            "total_registered": 0
        }
        
        try:
            # 生成模板因子
            if template_count > 0:
                template_factors = self.generate_factors_from_templates(template_count)
                results["template_generated"] = len(template_factors)
                results["template_registered"] = self.register_generated_factors(template_factors)
            
            # 生成AI增强因子
            if ai_count > 0:
                ai_factors = self.generate_ai_enhanced_factors(ai_count)
                results["ai_generated"] = len(ai_factors)
                results["ai_registered"] = self.register_generated_factors(ai_factors)
            
            results["total_registered"] = results["template_registered"] + results["ai_registered"]
            
            logger.info(f"因子生成完成: 总共注册 {results['total_registered']} 个新因子")
            
        except Exception as e:
            logger.error(f"自动因子生成过程出错: {e}")
        
        return results
    
    def get_generated_factors_summary(self) -> Dict[str, Any]:
        """获取已生成因子的摘要"""
        if not self.generated_factors:
            return {"message": "尚未生成任何因子"}
        
        summary = {
            "total_factors": len(self.generated_factors),
            "factors_by_type": {},
            "factors_by_method": {},
            "average_effectiveness": 0.0,
            "factors_list": []
        }
        
        total_effectiveness = 0.0
        
        for factor in self.generated_factors:
            info = factor.generation_info
            
            # 按类型统计
            factor_type = info.factor_type.value
            summary["factors_by_type"][factor_type] = summary["factors_by_type"].get(factor_type, 0) + 1
            
            # 按方法统计
            method = info.generation_method.value
            summary["factors_by_method"][method] = summary["factors_by_method"].get(method, 0) + 1
            
            # 累计效果
            total_effectiveness += info.expected_effectiveness
            
            # 因子列表
            summary["factors_list"].append({
                "name": info.factor_name,
                "type": factor_type,
                "method": method,
                "effectiveness": info.expected_effectiveness,
                "description": info.description[:100] + "..." if len(info.description) > 100 else info.description
            })
        
        summary["average_effectiveness"] = total_effectiveness / len(self.generated_factors)
        
        return summary


