    def generate_factors_from_templates(self, count: int = 3, 
                                      factor_types: List[FactorType] = None) -> List[GeneratedFactorInfo]:
        """基于模板生成因子"""
        logger.info(f"开始基于模板生成 {count} 个因子...")
        
        if factor_types is None:
            factor_types = [FactorType.MOMENTUM, FactorType.VOLUME, FactorType.VOLATILITY, 
                          FactorType.MEAN_REVERSION, FactorType.TECHNICAL]
        
        generated_factors = []
        
        for i in range(count):
            try:
                # 随机选择因子类型和模板
                target_type = random.choice(factor_types)
                suitable_templates = [t for t in self.templates if t.factor_type == target_type]
                
                if not suitable_templates:
                    continue
                
                template = random.choice(suitable_templates)
                
                # 生成随机参数
                parameters = {}
                for param_name, (min_val, max_val) in template.parameter_ranges.items():
                    if isinstance(min_val, int) and isinstance(max_val, int):
                        parameters[param_name] = random.randint(min_val, max_val)
                    else:
                        parameters[param_name] = round(random.uniform(min_val, max_val), 3)
                
                # 生成因子名称和描述
                factor_name = template.name_pattern.format(**parameters)
                description = template.description_pattern.format(**parameters)
                
                # 确保因子名称唯一
                factor_id = f"auto_{factor_name}_{self._generate_unique_id()}"
                
                # 计算预期有效性
                expected_effectiveness = self._estimate_factor_effectiveness(template, parameters)
                
                # 创建因子信息
                factor_info = GeneratedFactorInfo(
                    factor_id=factor_id,
                    factor_name=factor_name,
                    factor_type=target_type,
                    description=description,
                    generation_method=GenerationMethod.TEMPLATE_BASED,
                    template_used=template.template_id,
                    parameters=parameters,
                    expected_effectiveness=expected_effectiveness,
                    generation_time=datetime.now(),
                    source_code=template.calculation_template
                )
                
                generated_factors.append(factor_info)
                logger.info(f"生成因子: {factor_name} (预期效果: {expected_effectiveness:.3f})")
                
            except Exception as e:
                logger.error(f"生成第{i+1}个因子时出错: {e}")
        
        logger.info(f"基于模板成功生成 {len(generated_factors)} 个因子")
        return generated_factors
    
