logger = logging.getLogger(__name__)

class FactorType(Enum):
    """因子类型"""
    TECHNICAL = "technical"
    FUNDAMENTAL = "fundamental"
    SENTIMENT = "sentiment"
    VOLUME = "volume"
    MOMENTUM = "momentum"
    MEAN_REVERSION = "mean_reversion"
    VOLATILITY = "volatility"
    CROSS_SECTIONAL = "cross_sectional"

class GenerationMethod(Enum):
    """生成方法"""
    TEMPLATE_BASED = "template_based"
    AI_ENHANCED = "ai_enhanced"
    HYBRID = "hybrid"

@dataclass
class FactorTemplate:
    """因子模板"""
    template_id: str
    name_pattern: str
    description_pattern: str
    factor_type: FactorType
    required_data: List[str]
    calculation_template: str
    parameter_ranges: Dict[str, Tuple[float, float]]
    complexity_level: int  # 1-5, 复杂度等级

@dataclass
class GeneratedFactorInfo:
    """生成因子信息"""
    factor_id: str
    factor_name: str
    factor_type: FactorType
    description: str
    generation_method: GenerationMethod
    template_used: Optional[str]
    parameters: Dict[str, Any]
    expected_effectiveness: float
    generation_time: datetime
    source_code: str

class AutoGeneratedFactor(BaseFactor):
    """自动生成的因子"""
    
    def __init__(self, info: GeneratedFactorInfo, calculation_func: Callable):
        super().__init__(
            name=info.factor_name,
            category=info.factor_type.value,
            description=info.description
        )
        self.generation_info = info
        self.calculation_func = calculation_func
        self.dependencies = ["price", "volume"]  # 默认依赖
        
    def calculate(self, data: Dict[str, pd.DataFrame], symbol: str, **kwargs) -> FactorValue:
        """计算因子值"""
        try:
            value = self.calculation_func(data, symbol, **self.generation_info.parameters)
            
            return FactorValue(
                symbol=symbol,
                factor_name=self.name,
                value=value,
                timestamp=datetime.now(),
                confidence=self.generation_info.expected_effectiveness,
                metadata={
                    'generation_method': self.generation_info.generation_method.value,
                    'template_used': self.generation_info.template_used,
                    'generation_time': self.generation_info.generation_time.isoformat()
                }
            )
            
        except Exception as e:
            logger.error(f"自动生成因子 {self.name} 计算失败: {e}")
            return FactorValue(symbol, self.name, 0.0, datetime.now(), 0.0)

